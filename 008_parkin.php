				共通しているのは中脳黒質と呼ばれる部分を通っている<br />
				運動神経の働きを助ける神経伝達物質であるドパミンが<br />
				不足することによって起こると考えられている点です。<br />
				ドパミンの不足により、筋強剛・無動・安静時振戦などの症状が出現します。<br />
				病気が進行すると、<br />
				脳幹部の青斑核という部分に病変が及びます。<br />
				青斑核に存在しているノルアドレナリンという神経伝達物質が不足してくると、<br />
