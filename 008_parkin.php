		<p2>
			<div style="padding-left:20px;">
				<br />
				<strong><u>症状</u></strong>
				<br />
				<br />
				①安静時振戦 Resting tremor<br />
				　振戦とは、要するに振えのことです。<br />
				　安静時と特別に注意書きがあるくらいですので、<br />
				　動いたり物を持ったりするときには振えは少なくなります。<br />
				　リラックスしてくつろいでいる時などに、最も強く症状が出ます。<br />
				　同じように振戦を来たす病気として、本態性振戦という病気がありますが、<br />
				　本態性振戦ではパーキンソニスムとは対照的に<br />
				　物を持ったり動いたりする時に振えが出現し、<br />
				　リラックスしている時やお酒を飲むことで軽くなります（姿勢時振戦）。<br />
				　パーキンソニスムの振戦は比較的ゆっくりとした動きで、<br />
				　「振え」というよりも「揺れ」ているように見えることがあります。<br />
				　字を書く時には振えてぎざぎざになるのではなく、<br />
				　むしろ小さな字になります（ぎざぎざの字は本態性振戦の特徴です）。<br />
				<br />
				②筋強剛 Rigidity<br />
				　パーキンソニスムでは、体が硬くなります。<br />
				　運動麻痺はないのですが、関節が動きにくくなり、<br />
				　すべりの悪くなった歯車が仕込まれているように、<br />
				　関節を動かすとがくがくとした抵抗があります。<br />
				　筋強剛は首や手足の関節に特によくみられ、歩く時には前かがみになり、<br />
				　腕を振ることができずに小刻みな歩行になります。<br />
				　例えは悪いですが、ペンギンに似た歩き方になります。<br />
				<br />
				③無動 Akinesia<br />
				　筋強剛の影響で、動きが乏しくなります。<br />
				　特に長い時間じっとしていると、<br />
				　次の動作がなかなか始められなくなります。<br />
				　顔面の筋肉も動きにくくなり、表情に乏しい仮面様顔貌になります。<br />
				　喋り口調も小声で、<br />
				　アクセントに欠ける一本調子なものになってしまいます。<br />
				<br />
				④姿勢反射障害・すくみ足<br />
				　ある程度病状が進行すると、<br />
				　姿勢を変換するための反射が鈍くなります。<br />
				　同じ動きをしていると、<br />
				　別の動きへ変えることが難しくなります。<br />
				　立ち止まっている姿勢から歩き出すのにひどく時間がかかり（すくみ足）、<br />
				　後方に体を引っ張ると引き足を出して踏みとどまることができずに<br />
				　転倒してしまいます<br />
				　（歩いている姿勢からはいかにも前に倒れやすそうに見えますが、<br />
				　実際は前にも後ろにもよく転倒します）。<br />
				　いったん歩き始めるとだんだんと加速してしまい、<br />
				　方向転換や歩行の停止が難しくなります。<br />
				<br />
				⑤自律神経症状<br />
				　迷走神経の中枢が冒されることもしばしばあり、<br />
				　便秘・排尿障害などの排泄機能障害が出現しやすくなります。<br />
				　体表の分泌物が多くなるため、顔が油っぽくなり、<br />
				　目やにが出やすくなります（脂漏性顔貌）。<br />
				　涎も多くなります。<br />
				　血圧の調節がうまくいかずに立ちくらみも起こりやすくなります（起立性低血圧）。<br />
				<br />
				⑥精神症状<br />
				　体の動きが乏しくなるのと並行するかのように、<br />
				　精神機能も停滞しがちになります。<br />
				　特殊な例を除けば認知症を来たすことはありませんが、<br />
				　思考が遅くなるため、<br />
				　知的機能が障害されたように見えることがあります。<br />
				　元気がなくなり、意欲や自発性が低下することもあります。<br />
			</div>
